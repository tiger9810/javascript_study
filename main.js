const score = 59;

if (score >= 80) {
    console.log('Great');

} else {
    console.log('Umm...');
}

// 条件演算子
// 条件式 ? trueの処理 : falseの処理
const value = 50;
value >= 80 ? console.log("great") : console.log('Umm...');

